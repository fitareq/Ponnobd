package com.youthfireit.ponnobd.models;

public class ProductImages {


    private final Integer id;
    private final String src;



    public ProductImages(Integer id, String src) {

        this.id = id;
        this.src = src;
    }



    public Integer getId() {

        return id;
    }



    public String getSrc() {

        return src;
    }




}
